/*!
 * \file logdet_def.cpp
 * \brief Logdet operator definition: one input self + one output out, fp32-only, ND.
 */
#include "register/op_def_registry.h"

namespace ops {
class Logdet : public OpDef {
public:
    explicit Logdet(const char* name) : OpDef(name)
    {
        this->Input("self")
            .ParamType(REQUIRED)
            .DataType({ge::DT_FLOAT})
            .Format({ge::FORMAT_ND})
            .UnknownShapeFormat({ge::FORMAT_ND})
            .AutoContiguous();

        this->Output("out")
            .ParamType(REQUIRED)
            .DataType({ge::DT_FLOAT})
            .Format({ge::FORMAT_ND})
            .UnknownShapeFormat({ge::FORMAT_ND});

        OpAICoreConfig aicoreConfig910B;
        aicoreConfig910B.DynamicCompileStaticFlag(true)
            .DynamicFormatFlag(false)
            .DynamicRankSupportFlag(true)
            .DynamicShapeSupportFlag(true)
            .NeedCheckSupportFlag(false)
            .PrecisionReduceFlag(true)
            .ExtendCfgInfo("opFile.value", "logdet");
        this->AICore().AddConfig("ascend910b", aicoreConfig910B);
    }
};
OP_ADD(Logdet);
} // namespace ops
