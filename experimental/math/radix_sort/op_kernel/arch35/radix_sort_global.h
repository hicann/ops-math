    uint32_t warpId = tid / PREFIX_WARP_SZ;
    uint32_t laneId = tid % PREFIX_WARP_SZ;

    int64_t localSum = globalHist[tid];
    int64_t warpPrefix = localSum;
    warpPrefix += ShflUpMask(warpPrefix, 1,  laneId);
    warpPrefix += ShflUpMask(warpPrefix, 2,  laneId);
    warpPrefix += ShflUpMask(warpPrefix, 4,  laneId);
    warpPrefix += ShflUpMask(warpPrefix, 8,  laneId);
    warpPrefix += ShflUpMask(warpPrefix, 16, laneId);

    if (laneId == PREFIX_WARP_SZ - 1) {
        ubWarpBuf[warpId] = warpPrefix;
    }
    asc_syncthreads();

