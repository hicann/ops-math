static int64_t ComputeUserWorkspaceSize(const RadixSortTilingData& tiling)
{
    int64_t userWs = 0;

    if (tiling.totalElements == 0) {
        return 0;
    }

    if (tiling.pathType == 1) {
        return 0;
    }

    if (tiling.pathType == 2 || tiling.pathType == 4) {
        // GLOBAL_SORT: global histogram + per-core hists + starts + ping-pong
        int64_t maxElemSize = 8;
        int64_t histCells = static_cast<int64_t>(tiling.needCoreNum) * RADIX_BINS;
        int64_t globalHistWs = AlignUp(RADIX_BINS * static_cast<int64_t>(sizeof(int64_t)), WS_ALIGN);
        int64_t prefixSumWs = AlignUp(RADIX_BINS * static_cast<int64_t>(sizeof(int64_t)), WS_ALIGN);
        int64_t perCoreHistsWs = AlignUp(histCells * static_cast<int64_t>(sizeof(int64_t)), WS_ALIGN);
        int64_t coreStartsWs = AlignUp(histCells * static_cast<int64_t>(sizeof(int64_t)), WS_ALIGN);
        int64_t keyAreaBytes = AlignUp(tiling.totalElements * maxElemSize, WS_ALIGN);
        int64_t idxAreaBytes = AlignUp(tiling.totalElements * static_cast<int64_t>(sizeof(int64_t)), WS_ALIGN);
        userWs = globalHistWs + prefixSumWs + perCoreHistsWs + coreStartsWs
            + keyAreaBytes + idxAreaBytes + keyAreaBytes + idxAreaBytes;
        return AlignUp(userWs, WS_ALIGN);
    }

    if (tiling.pathType == 3) {
        int64_t maxElemSize = 8;
        int64_t scatterWs = AlignUp(
            tiling.totalElements * maxElemSize * 4, WS_ALIGN);
        userWs = AlignUp(tiling.wsScatterOffset + scatterWs, WS_ALIGN);
        return userWs;
    }

    return 0;
}

static void ComputeRadixSortTiling(RadixSortTilingData& tiling,
    int64_t totalElements, bool descending, int64_t physicalCoreNum,
    int64_t ubSortBuf)
{
    if (totalElements == 0) {
        tiling.needCoreNum = 1;
        tiling.totalElements = 0;
        tiling.perCoreElements = 0;
        tiling.pathType = 0;
        tiling.descending = descending;
        tiling.totalTiles = 0;
        tiling.wsScatterOffset = 0;
        return;
    }

    int64_t perCore = CeilDiv(totalElements, physicalCoreNum);
    if (perCore < PER_CORE_MIN) {
        perCore = PER_CORE_MIN;
    }
    int64_t needCore = CeilDiv(totalElements, perCore);

    int32_t pathType;
    int32_t totalTiles = 0;
    if (needCore == 1) {
        pathType = (totalElements <= ubSortBuf) ? 1 : 3;
        if (pathType == 3) {
            totalTiles = static_cast<int32_t>(
                CeilDiv(totalElements, ubSortBuf));
        }
    } else {
        pathType = (perCore <= ubSortBuf) ? 2 : 4;
        if (pathType == 4) {
            totalTiles = static_cast<int32_t>(
                CeilDiv(perCore, ubSortBuf));
        }
    }

    // Compute wsScatterOffset: per-core histogram area at offset 0, scatter after it
    int64_t histAreaBytes = static_cast<int64_t>(totalTiles) * static_cast<int64_t>(RADIX_BINS)
        * static_cast<int64_t>(sizeof(int32_t));
    int64_t histAligned = AlignUp(histAreaBytes, static_cast<int64_t>(WS_ALIGN));
    int64_t wsScatterOffset = (pathType >= 3)
        ? AlignUp(static_cast<int64_t>(WS_HIST_OFFSET)
            + static_cast<int64_t>(needCore) * histAligned, static_cast<int64_t>(WS_ALIGN))
        : 0;

    tiling.needCoreNum = static_cast<int32_t>(needCore);
    tiling.totalElements = totalElements;
    tiling.perCoreElements = perCore;
    tiling.pathType = pathType;
    tiling.descending = descending;
    tiling.totalTiles = totalTiles;
    tiling.wsScatterOffset = wsScatterOffset;
    tiling.wsElemSize = 8;
}

