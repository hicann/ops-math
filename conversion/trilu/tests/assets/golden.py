def trilu_golden(x, k=None, *, upper=0, **kwargs):
    '''
    Golden function for trilu.
    All the parameters (names and order) follow @trilu_def.cpp without outputs.
    All the input Tensors are numpy.ndarray.

    Args:
        x: 输入张量 (numpy.ndarray)，任意维度，最后两维为矩阵
        k: 可选输入张量 (numpy.ndarray 或 None)，对角线偏移量标量，int32/int64。
           None 时 k=0（主对角线）
        upper: 属性，1=上三角，0=下三角。默认 0

        **kwargs: {input,output}_{dtypes,ori_shapes,formats,ori_formats},
                  full_soc_version, short_soc_version, testcase_name

    Returns:
        Output tensor
    '''
    if k is not None:
        diagonal = int(k.item())
    else:
        diagonal = 0

    x_dtype = x.dtype
    x_ori_shape = x.shape

    if x_dtype.name in ("bfloat16", "float16"):
        x = x.astype(np.float32)
    elif x_dtype.name == "uint16":
        x = x.astype(np.int32)

    if x.ndim < 2:
        x = x.reshape(1, -1) if x.ndim == 1 else x.reshape(1, 1)

    x_torch = torch.from_numpy(x)
    if upper:
        output_y = torch.triu(x_torch, diagonal=diagonal)
    else:
        output_y = torch.tril(x_torch, diagonal=diagonal)

    result = output_y.numpy()
    if len(x_ori_shape) < 2:
        result = result.reshape(x_ori_shape)
    if x_dtype.name in ("bfloat16", "float16", "uint16"):
        result = result.astype(x_dtype, copy=False)

    return result
