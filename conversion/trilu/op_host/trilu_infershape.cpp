static ge::graphStatus InferShapeTrilu(gert::InferShapeContext* context)
{
    const gert::Shape* xShape = context->GetInputShape(0);
    gert::Shape* yShape = context->GetOutputShape(0);

    if (xShape == nullptr || yShape == nullptr) {
        OP_LOGE(context->GetNodeName(), "Input or Output shape is null");
        return GRAPH_FAILED;
    }

    size_t dimNum = xShape->GetDimNum();
    yShape->SetDimNum(dimNum);
    for (size_t i = 0; i < dimNum; i++) {
        yShape->SetDim(i, xShape->GetDim(i));
    }
    return GRAPH_SUCCESS;
}
IMPL_OP_INFERSHAPE(Trilu).InferShape(InferShapeTrilu);
