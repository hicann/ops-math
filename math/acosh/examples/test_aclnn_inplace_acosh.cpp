#include <cmath>
#include <cstdio>
#include <iostream>
#include <vector>

#include "acl/acl.h"
#include "aclnn_acosh.h"

#define CHECK_RET(cond, return_expr)            \
    do {                                        \
        if (!(cond)) {                          \
            return_expr;                        \
        }                                       \
    } while (0)

#define LOG_PRINT(message, ...)                 \
