// Quantize float to FP16 and back (simulates FP16 input quantization)
static float QuantizeToFP16(float f)
{
    uint32_t bits;
    memcpy(&bits, &f, 4);
    uint16_t sign = (bits >> 16) & 0x8000;
    int32_t exp = ((bits >> 23) & 0xff) - 127 + 15;
    uint32_t mant = bits & 0x007fffff;
    uint16_t h;
    if (exp <= 0) {
        if (exp < -10)
            h = sign;
        else {
            mant |= 0x00800000;
            h = sign | (uint16_t)(mant >> (14 - exp));
        }
    } else if (exp >= 31) {
        h = sign | 0x7c00 | (mant ? 0x0200 : 0);
    } else {
        h = sign | (uint16_t)(exp << 10) | (uint16_t)(mant >> 13);
    }
    // Convert back to float
    uint32_t rsign = (h & 0x8000) << 16;
    uint32_t rexp = (h >> 10) & 0x1f;
    uint32_t rmant = h & 0x3ff;
    uint32_t result;
    if (rexp == 0)
        result = rmant ? (rsign | 0x38000000 | (rmant << 13)) : rsign;
    else if (rexp == 31)
        result = rsign | 0x7f800000 | (rmant << 13);
    else
        result = rsign | ((rexp + 112) << 23) | (rmant << 13);
    float r;
    memcpy(&r, &result, 4);
    return r;
}

// Quantize float to BF16 and back (simulates BF16 input quantization)
static float QuantizeToBF16(float f)
{
    uint32_t bits;
    memcpy(&bits, &f, 4);
    uint16_t h = (uint16_t)(bits >> 16); // truncate lower 16 bits
    uint32_t result = (uint32_t)h << 16;
    float r;
    memcpy(&r, &result, 4);
    return r;
}

bool RunPrecisionTest(const TestConfig& cfg, const vector<float>& input_data)
{
    int64_t size = input_data.size();
    vector<float> golden(size), npu_output(size);

    for (int64_t i = 0; i < size; i++) {
        // For FP16/BF16, compute golden from the QUANTIZED input
        // (the kernel receives the quantized value, not the original FP32 value)
        float quantized_input = input_data[i];
        if (cfg.dtype == ge::DT_FLOAT16) {
            quantized_input = QuantizeToFP16(input_data[i]);
        } else if (cfg.dtype == ge::DT_BF16) {
            quantized_input = QuantizeToBF16(input_data[i]);
        }
        golden[i] = (float)FresnelCosGolden(quantized_input);
    }

    int ret = CreateAndRunGraph(cfg.dtype, input_data, npu_output);
    if (ret != SUCCESS) {
        printf("  [FAIL] %s: graph execution failed\n", cfg.name.c_str());
        return false;
    }

    int fail_count = 0;
    double max_rel = 0, max_abs = 0;
    for (int64_t i = 0; i < size; i++) {
        double g = golden[i], a = npu_output[i];
        if (std::isnan(g) && std::isnan(a))
            continue;
        if (std::isnan(g) || std::isnan(a)) {
            fail_count++;
            continue;
        }
        if (std::isinf(g) && std::isinf(a) && (g > 0) == (a > 0))
            continue;
        double abs_err = std::abs(a - g);
        double rel_err = abs_err / (std::abs(g) + 1e-30);
        if (abs_err > max_abs)
            max_abs = abs_err;
        if (rel_err > max_rel)
            max_rel = rel_err;
        if (abs_err > cfg.atol && rel_err > cfg.rtol) {
            fail_count++;
            if (fail_count <= 3) {
                printf("  [FAIL] [%ld] x=%.4f golden=%.6e actual=%.6e abs=%.2e rel=%.2e\n", (long)i, input_data[i], g,
                       a, abs_err, rel_err);
            }
        }
    }

    if (fail_count == 0) {
        printf("  [PASS] %s: %ld elems, max_rel=%.2e, max_abs=%.2e (rtol=%.1e, atol=%.1e)\n", cfg.name.c_str(),
               (long)size, max_rel, max_abs, cfg.rtol, cfg.atol);
        return true;
    } else {
        printf("  [FAIL] %s: %d/%ld elements exceed tolerance\n", cfg.name.c_str(), fail_count, (long)size);
        return false;
    }
}

