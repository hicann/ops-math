// Cephes Fresnel Cosine (double precision golden)
double FresnelCosGolden(double x)
{
    static const double CN[6] = {9.99999999999999998822E-1,  -2.05525900955013891793E-1, 1.88843319396703850064E-2,
                                 -6.45191435683965050962E-4, 9.50428062829859605134E-6,  -4.98843114573573548651E-8};
    static const double CD[7] = {1.00000000000000000118E0,  4.12142090722199792936E-2, 8.68029542941784300606E-4,
                                 1.22262789024179030997E-5, 1.25001862479598821474E-7, 9.15439215774657478799E-10,
                                 3.99982968972495980367E-12};
    static const double FN[10] = {3.76329711269987889006E-20, 1.34283276233062758925E-16, 1.72010743268161828879E-13,
                                  1.02304514164907233465E-10, 3.05568983790257605827E-8,  4.63613749287867322088E-6,
                                  3.45017939782574027900E-4,  1.15220955073585758835E-2,  1.43407919780758885261E-1,
                                  4.21543555043677546506E-1};
    static const double FD[11] = {1.25443237090011264384E-20,
                                  4.52001434074129701496E-17,
                                  5.88754533621578410010E-14,
                                  3.60140029589371370404E-11,
                                  1.12699224763999035261E-8,
                                  1.84627567348930545870E-6,
                                  1.55934409164153020873E-4,
                                  6.44051526508858611005E-3,
                                  1.16888925859191382142E-1,
                                  7.51586398353378947175E-1,
                                  1.0};
    static const double GN[11] = {1.86958710162783235106E-22, 8.36354435630677421531E-19, 1.37555460633261799868E-15,
                                  1.08268041139020870318E-12, 4.45344415861750144738E-10, 9.82852443688422223854E-8,
                                  1.15138826111884280931E-5,  6.84079380915393090172E-4,  1.87648584092575249293E-2,
                                  1.97102833525523411709E-1,  5.04442073643383265887E-1};
    static const double GD[12] = {1.86958710162783236342E-22, 8.39158816283118707363E-19, 1.38796531259578871258E-15,
                                  1.10273215066240270757E-12, 4.60680728146520428211E-10, 1.04314589657571990585E-7,
                                  1.27545075667729118702E-5,  8.14679107184306179049E-4,  2.53603741420338795122E-2,
                                  3.37748989120019970451E-1,  1.47495759925128324529E0,   1.0};
    const double PI = 3.14159265358979323846;
    const double HALF_PI = 1.57079632679489661923;

    if (std::isnan(x))
        return std::nan("");
    if (std::isinf(x))
        return (x > 0) ? 0.5 : -0.5;

    double sign = (x < 0) ? -1.0 : 1.0;
    double ax = std::abs(x);

    if (ax > 36974.0)
        return sign * 0.5;

    double x2 = ax * ax;
    double result;

    if (x2 < 2.5625) {
        double x4 = x2 * x2;
        double pn = CN[5];
        for (int i = 4; i >= 0; i--)
            pn = pn * x4 + CN[i];
        double pd = CD[6];
        for (int i = 5; i >= 0; i--)
            pd = pd * x4 + CD[i];
        result = ax * pn / pd;
    } else {
        double t = 1.0 / (PI * x2);
        double u = t * t;
        double fn_val = FN[9];
        for (int i = 8; i >= 0; i--)
            fn_val = fn_val * u + FN[i];
        double fd_val = FD[10];
        for (int i = 9; i >= 0; i--)
            fd_val = fd_val * u + FD[i];
        double f = 1.0 - u * fn_val / fd_val;
        double gn_val = GN[10];
        for (int i = 9; i >= 0; i--)
            gn_val = gn_val * u + GN[i];
        double gd_val = GD[11];
        for (int i = 10; i >= 0; i--)
            gd_val = gd_val * u + GD[i];
        double g = t * gn_val / gd_val;
        double z = HALF_PI * x2;
        result = 0.5 + (f * std::sin(z) - g * std::cos(z)) / (PI * ax);
    }
    return sign * result;
}

