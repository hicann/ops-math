/*!
 * \file acos_grad_def.cpp
 * \brief AcosGrad operator definition (aligned with proto.h: y / dy / z)
 *
 * AcosGrad(y, dy) = -dy / sqrt(1 - y^2)   (element-wise)
 *   y  : forward input of Acos (canndev convention)
 *   dy : upstream gradient
 *   z  : gradient w.r.t. the original input
 */
#include "register/op_def_registry.h"

namespace ops {
class AcosGrad : public OpDef {
public:
    explicit AcosGrad(const char* name) : OpDef(name)
    {
        const std::vector<ge::DataType> AcosGradDataType = {
            ge::DT_FLOAT16, ge::DT_FLOAT, ge::DT_BF16};
        const std::vector<ge::Format> AcosGradFormat = {
            ge::FORMAT_ND, ge::FORMAT_ND, ge::FORMAT_ND};

        this->Input("y")
            .ParamType(REQUIRED)
            .DataType(AcosGradDataType)
            .Format(AcosGradFormat)
            .UnknownShapeFormat({ge::FORMAT_ND, ge::FORMAT_ND, ge::FORMAT_ND})
            .AutoContiguous();

        this->Input("dy")
            .ParamType(REQUIRED)
            .DataType(AcosGradDataType)
            .Format(AcosGradFormat)
            .UnknownShapeFormat({ge::FORMAT_ND, ge::FORMAT_ND, ge::FORMAT_ND})
            .AutoContiguous();

        this->Output("z")
            .ParamType(REQUIRED)
            .DataType(AcosGradDataType)
            .Format(AcosGradFormat)
            .UnknownShapeFormat({ge::FORMAT_ND, ge::FORMAT_ND, ge::FORMAT_ND})
            .AutoContiguous();

        OpAICoreConfig aiCoreConfig;
        aiCoreConfig.DynamicCompileStaticFlag(true)
            .DynamicFormatFlag(false)
            .DynamicShapeSupportFlag(true)
            .PrecisionReduceFlag(true)
            .NeedCheckSupportFlag(false)
            .DynamicRankSupportFlag(true)
            .ExtendCfgInfo("opFile.value", "acos_grad_apt");
        this->AICore().AddConfig("ascend950", aiCoreConfig);
    }
};
OP_ADD(AcosGrad);
} // namespace ops
