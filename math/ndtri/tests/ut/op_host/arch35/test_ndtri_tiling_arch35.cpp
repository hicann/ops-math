// ==========================================================================
// 主路径：FP32 对齐 + 多核
//   shape {1, 64, 2, 64} = 8192 elem
//   FP32: alignElem = 32/4 = 8
//   8192 % 8 == 0 → isAlign=1
//   8192 >= alignElem → 多核
// 期望 tilingData: "8192 <blockFactor> <ubFactor> "
//   coreNum=64: perCoreRaw=ceil(8192/64)=128，blockFactor=ceil_align(128,8)=128
//   ubFactor: availableUb=(262144-49152)/80=2662 → floor_align(2662,256)=2560
// ==========================================================================
TEST_F(NdtriTilingTest, test_tiling_fp32_align_001)
{
    optiling::NdtriCompileInfoStub compileInfo;
    gert::TilingContextPara tilingContextPara(
        "Ndtri",
        {
            {{{1, 64, 2, 64}, {1, 64, 2, 64}}, ge::DT_FLOAT, ge::FORMAT_ND},
        },
        {
            {{{1, 64, 2, 64}, {1, 64, 2, 64}}, ge::DT_FLOAT, ge::FORMAT_ND},
        },
        &compileInfo);
    // TilingKey = 256：fp32 + isAlign=1（由 ASCENDC_TPL_SEL_PARAM 编码）
    uint64_t expectTilingKey = 256;
    string expectTilingData = "8192 128 2560 ";
    std::vector<size_t> expectWorkspaces = {16777216};
    ExecuteTestCase(tilingContextPara, ge::GRAPH_SUCCESS, expectTilingKey, expectTilingData, expectWorkspaces);
}

