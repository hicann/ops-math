/*!
 * \file test_ndtri_tiling_arch35.cpp
 * \brief Ndtri Tiling UT（arch35 / Ascend950）
 *
 * 覆盖路径（参见 op_host/arch35/ndtri_tiling_arch35.cpp）:
 *   1) dtype 路径（DispatchTilingKey）：FP32 / FP16 / BF16 → C_DT_FLOAT/C_DT_FLOAT16/C_DT_BF16
 *   2) isAlign 路径（DispatchTilingKey）：
 *        - totalNum % alignElem == 0 → isAlign=1
 *        - totalNum % alignElem != 0 → isAlign=0
 *   3) 多核 vs 单核（DoTiling）：
 *        - totalNum <  alignElem → 单核 usedCoreNum=1
 *        - totalNum >= alignElem → 多核
 *   4) 失败路径：
 *        - 不支持 dtype（INT32/DOUBLE 等）→ CheckDtype 返回 GRAPH_FAILED
 *        - totalNum=0（zero-shape）→ GetTotalNum 返回 GRAPH_FAILED
 *
 * 期望 tilingData 字段（NdtriTilingData，定义在 op_kernel/arch35/ndtri_tiling_data.h）：
 *   "totalNum blockFactor ubFactor "
 *
 * 期望 TilingKey: ASCENDC_TPL_SEL_PARAM(context, dtypeKey, isAlign)
 *   dtypeKey: 0=C_DT_FLOAT, 1=C_DT_FLOAT16, 2=C_DT_BF16（具体取决于框架编码）
 *   实际 TilingKey 由框架根据 (dtypeKey, isAlign) 计算，本 UT 通过 expectTilingKey 固化运行时实际值。
 */

