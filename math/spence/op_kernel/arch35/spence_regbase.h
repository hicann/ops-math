#include <type_traits>

using namespace AscendC;

constexpr uint32_t SPENCE_CHUNK = 256;  // elements per chunk
constexpr float PI2_OVER_6 = 1.6449340668482264f;
constexpr float POS_INF_F = INFINITY;
constexpr float SAFE_EPS_F = 1.0e-30f;
constexpr float HALF_F = 0.5f;
constexpr float ONE_AND_HALF_F = 1.5f;
constexpr float NEG_HALF_F = -0.5f;

// Cephes rational polynomial coefficients (from dilog.c / golden.py)
// A coefficients (numerator), Horner order: A[0]=highest degree
constexpr float CEPHES_A0 = 4.65128586073990045278E-5f;
constexpr float CEPHES_A1 = 7.31589045238094711071E-3f;
constexpr float CEPHES_A2 = 1.33847639578309018650E-1f;
constexpr float CEPHES_A3 = 8.79691311754530315341E-1f;
constexpr float CEPHES_A4 = 2.71149851196553469920E0f;
constexpr float CEPHES_A5 = 4.25697156008121755724E0f;
constexpr float CEPHES_A6 = 3.29771340985225106936E0f;
constexpr float CEPHES_A7 = 1.00000000000000000126E0f;

// B coefficients (denominator), Horner order: B[0]=highest degree
constexpr float CEPHES_B0 = 6.90990488912553276999E-4f;
constexpr float CEPHES_B1 = 2.54043763932544379113E-2f;
constexpr float CEPHES_B2 = 2.82974860602568089943E-1f;
constexpr float CEPHES_B3 = 1.41172597751831069617E0f;
constexpr float CEPHES_B4 = 3.63800533345137075418E0f;
constexpr float CEPHES_B5 = 5.03278880143316990390E0f;
constexpr float CEPHES_B6 = 3.54771340985225096217E0f;
constexpr float CEPHES_B7 = 9.99999999999999998740E-1f;

