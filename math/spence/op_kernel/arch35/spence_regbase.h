/*!
 * \file spence_regbase.h
 * \brief Spence 算子 Kernel 实现（arch35 / ascend950 架构）
 *
 * 路线：RegBase — __VEC_SCOPE__ + RegTensor（__vector__ kernel entry）
 *
 * 模板参数 T：输入/输出数据类型（float / half / bfloat16_t）
 * - FP32 路径：直算，无需 Cast
 * - FP16/BF16 路径：Cast→FP32 → VF 计算 → Cast 回原始类型
 *
 * 算法：Cephes 7 阶有理多项式 + 恒等式映射（与 golden.py 完全一致）
 *
 *   Step 1: y = x if x < 2, else 1/x          → y ∈ (0, 2)
 *   Step 2: w = 1/y - 1  if y > 1.5
 *           w = -y       if y < 0.5
 *           w = y - 1    if 0.5 ≤ y ≤ 1.5      → |w| ≤ 0.5
 *   Step 3: spence_pol = -w * A(w) / B(w)      (Cephes rational polynomial)
 *   Step 4: if y < 0.5: spence_1 = π²/6 - ln(y)*ln(1-y) - spence_pol
 *           else:        spence_1 = spence_pol
 *   Step 5: if x > 1.5: result = -0.5*ln²(y) - spence_1
 *           else:        result = spence_1
 *
 *   特殊值：x=1→0, x=0→π²/6, x<0→NaN, x=+inf→-inf
 *
 * 参考：Cephes math library (dilog.c), scipy.special.spence, TF spence_op
 */

