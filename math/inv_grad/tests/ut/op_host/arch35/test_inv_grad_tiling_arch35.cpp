/*!
 * \file test_inv_grad_tiling_arch35.cpp
 * \brief InvGrad Tiling UT (arch35 / Ascend950)
 *
 * Covers paths in op_host/arch35/inv_grad_tiling_arch35.cpp:
 *   1) Dtype path -- TilingKey selected by ASCENDC_TPL_SEL_PARAM(context, dTypeX)
 *        - FP32  -> D_T_X = C_DT_FLOAT
 *        - FP16  -> D_T_X = C_DT_FLOAT16
 *        - BF16  -> D_T_X = C_DT_BF16
 *      Per header doc inv_grad_tiling_key.h, mapping is 0/1/2; the actual
 *      runtime TilingKey is framework-determined and fixed in expectTilingKey.
 *   2) Multi-core path (large shape) vs single-core path (small shape).
 *   3) Non-aligned tail (totalElements not multiple of 32B in elements).
 *   4) Empty tensor (totalElements == 0)
 *        - kernel early-return path: blockDim=1, all tiling fields = 0.
 *   5) Failure paths:
 *        - dtype mismatch between x and grad   -> GRAPH_FAILED
 *        - shape mismatch (dimNum differs)     -> GRAPH_FAILED
 *        - shape mismatch (same rank, dim differs) -> GRAPH_FAILED
 *        - unsupported dtype (DT_DOUBLE)       -> GRAPH_FAILED
 *
 * Default platform info from TilingContextPara: coreNum=64, ubSize=262144.
 *
 * Expected TilingData = "totalElements blockFactor ubFactor " (3 int64_t).
 *
 * UB / blockFactor derivation (matches inv_grad_tiling_arch35.cpp lines 200-207):
 *   bytesPerElem = 3 * typeSize + 2 * sizeof(float)
 *   FP32 typeSize=4 -> bytesPerElem=20 -> ubFactor=FloorAlign(262144/20, 8)
 *                                                 = FloorAlign(13107, 8) = 13104
 *   FP16 typeSize=2 -> bytesPerElem=14 -> ubFactor=FloorAlign(262144/14, 16)
 *                                                 = FloorAlign(18724, 16) = 18720
 *   BF16 typeSize=2 -> same formula as FP16        -> ubFactor=18720
 *
 *   blockFactor = ceil_align( ceil(totalElements/coreNum), ubBlockSize )
 *   usedCoreNum = ceil(totalElements / blockFactor)
 *
 * The included header re-exposes InvGradCompileInfo (an empty marker struct)
 * matching the TilingParse<InvGradCompileInfo> registration.
 */

