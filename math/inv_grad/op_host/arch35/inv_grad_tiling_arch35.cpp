/*!
 * \file inv_grad_tiling_arch35.cpp
 * \brief InvGrad tiling implementation (arch35 / Ascend950)
 *
 * Tiling strategy:
 *   1. Multi-core: divide total elements evenly across AI Cores
 *   2. UB: divide per-core elements into UB-sized chunks
 *   3. Buffer layout (unified for all dtypes):
 *      xQueue(1)    = ubFactor * sizeof(T)
 *      gradQueue(1) = ubFactor * sizeof(T)
 *      outQueue(1)  = ubFactor * sizeof(T)
 *      tmpBuf1      = ubFactor * sizeof(float)   (fp32 intermediate xx / xFloat)
 *      tmpBuf2      = ubFactor * sizeof(float)   (fp32 intermediate gradFloat)
 *
 * Unified formula for all dtypes:
 *   bytesPerElem = 3 * sizeof(T) + 2 * sizeof(float)
 *   ubFactor = FloorAlign(ubSize / bytesPerElem, ubBlockSize)
 *
 * Shape/dtype equality between x and grad is enforced here.
 * InferShape uses the broadcast util in op_host/inv_grad_infershape.cpp.
 */

