static std::string GetTime()
{
    time_t timep;
    time(&timep);
    char tmp[64];
    strftime(tmp, sizeof(tmp), "%Y-%m-%d %H:%M:%S,000", localtime(&timep));
    return tmp;
}

static uint32_t GetDataTypeSize(DataType dt)
{
    switch (dt) {
        case DT_FLOAT:
            return 4;
        case DT_FLOAT16:
            return 2;
        default:
            return 4;
    }
}

static int32_t GenTestData(const std::vector<int64_t>& shapes, Tensor& tensor, TensorDesc& desc, DataType dtype)
{
    desc.SetRealDimCnt(shapes.size());
    size_t size = 1;
    for (auto d : shapes)
        size *= d;

    size_t data_len = size * GetDataTypeSize(dtype);
    float* pData = new (std::nothrow) float[size];
    if (pData == nullptr)
        return FAILED;
    for (size_t i = 0; i < size; ++i) {
        pData[i] = static_cast<float>(i % 10) * 0.5f;
    }
    tensor = Tensor(desc, reinterpret_cast<uint8_t*>(pData), data_len);
    delete[] pData;
    return SUCCESS;
}

static int32_t WriteDataToFile(const std::string& bin_file, uint64_t data_size, uint8_t* data)
{
    FILE* fp = fopen(bin_file.c_str(), "wb");
    if (fp == nullptr)
        return FAILED;
    fwrite(data, sizeof(uint8_t), data_size, fp);
    fclose(fp);
    return SUCCESS;
}

int CreateOpInGraph(DataType inDtype, std::vector<ge::Tensor>& input, std::vector<Operator>& inputs,
                    std::vector<Operator>& outputs, Graph& graph)
{
    Status ret = SUCCESS;
    auto bessel_op = op::BesselI1e("bessel_i1e_0");

    std::vector<int64_t> xShape = {32, 4, 4, 4};
    auto placeholder0 = op::Data("placeholder0").set_attr_index(0);
    TensorDesc placeholder0_desc = TensorDesc(ge::Shape(xShape), FORMAT_ND, inDtype);
    placeholder0_desc.SetPlacement(ge::kPlacementHost);
    placeholder0_desc.SetFormat(FORMAT_ND);
    Tensor tensor_placeholder0;
    ret = GenTestData(xShape, tensor_placeholder0, placeholder0_desc, inDtype);
    if (ret != SUCCESS) {
        printf("%s - ERROR: Generate input data failed\n", GetTime().c_str());
        return FAILED;
    }
    placeholder0.update_input_desc_x(placeholder0_desc);
    input.push_back(tensor_placeholder0);
    graph.AddOp(placeholder0);
    bessel_op.set_input_x(placeholder0);

    TensorDesc y_desc = TensorDesc(ge::Shape(xShape), FORMAT_ND, inDtype);
    bessel_op.update_output_desc_y(y_desc);

    inputs.push_back(placeholder0);
    outputs.push_back(bessel_op);
    return SUCCESS;
}

int main(int argc, char* argv[])
{
    const char* graph_name = "bessel_i1e_geir_test";
    Graph graph(graph_name);
    std::vector<ge::Tensor> input;

    printf("%s - INFO: Start to initialize GE\n", GetTime().c_str());
    std::map<AscendString, AscendString> global_options = {{"ge.exec.deviceId", "0"}, {"ge.graphRunMode", "1"}};
    Status ret = ge::GEInitialize(global_options);
    if (ret != SUCCESS) {
        printf("%s - ERROR: GE initialize failed\n", GetTime().c_str());
        return FAILED;
    }

    std::vector<Operator> inputs{};
    std::vector<Operator> outputs{};
    DataType inDtype = DT_FLOAT;

    ret = CreateOpInGraph(inDtype, input, inputs, outputs, graph);
    if (ret != SUCCESS) {
        printf("%s - ERROR: Create op in graph failed\n", GetTime().c_str());
        ge::GEFinalize();
        return FAILED;
    }

    if (!inputs.empty() && !outputs.empty()) {
        graph.SetInputs(inputs).SetOutputs(outputs);
    }

    std::map<AscendString, AscendString> build_options = {};
    printf("%s - INFO: Creating session\n", GetTime().c_str());
    ge::Session* session = new Session(build_options);
    if (session == nullptr) {
        printf("%s - ERROR: Create session failed\n", GetTime().c_str());
        ge::GEFinalize();
        return FAILED;
    }

    std::map<AscendString, AscendString> graph_options = {};
    uint32_t graph_id = 0;
    ret = session->AddGraph(graph_id, graph, graph_options);
    if (ret != SUCCESS) {
        printf("%s - ERROR: Add graph failed\n", GetTime().c_str());
        delete session;
        ge::GEFinalize();
        return FAILED;
    }

    printf("%s - INFO: Running graph\n", GetTime().c_str());
    std::vector<ge::Tensor> output;
    ret = session->RunGraph(graph_id, input, output);
    if (ret != SUCCESS) {
        printf("%s - ERROR: Run graph failed\n", GetTime().c_str());
        delete session;
        ge::GEFinalize();
        return FAILED;
    }
    printf("%s - INFO: Run graph success\n", GetTime().c_str());

    for (size_t i = 0; i < output.size(); i++) {
        std::string output_file = "./bessel_i1e_geir_output_" + std::to_string(i) + ".bin";
        uint8_t* output_data = output[i].GetData();
        int64_t output_shape_size = output[i].GetTensorDesc().GetShape().GetShapeSize();
        uint64_t data_size = static_cast<uint64_t>(output_shape_size) *
                             GetDataTypeSize(output[i].GetTensorDesc().GetDataType());
        WriteDataToFile(output_file, data_size, output_data);
        printf("%s - INFO: Output %zu written to %s (size=%lu bytes)\n", GetTime().c_str(), i, output_file.c_str(),
               data_size);
    }

    printf("%s - INFO: Finalizing\n", GetTime().c_str());
    delete session;
    ret = ge::GEFinalize();
    if (ret != SUCCESS) {
        printf("%s - ERROR: GE finalize failed\n", GetTime().c_str());
        return FAILED;
    }
    printf("%s - INFO: Done\n", GetTime().c_str());
    return SUCCESS;
}
