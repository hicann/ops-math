        AscendC::SetFlag<AscendC::HardEvent::MTE2_V>(ev.mte2toV);
        AscendC::WaitFlag<AscendC::HardEvent::MTE2_V>(ev.mte2toV);
        AscendC::Cast(buf_[B2].template Get<float>(), buf_[B0].template Get<T>(),
                      AscendC::RoundMode::CAST_NONE, count);
