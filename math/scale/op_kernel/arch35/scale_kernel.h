            // WAR 后 S2a CopyIn bias→B0; S2b Cast B0→B3(累加器); S2c MulAddDst(B3 ← B1·B2 + B3)
            AscendC::SetFlag<AscendC::HardEvent::V_MTE2>(ev.vtoMte2);
            AscendC::WaitFlag<AscendC::HardEvent::V_MTE2>(ev.vtoMte2);
